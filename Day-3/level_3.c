#include <stdio.h>

void main() {
    int x, y, z;

    printf("Enter value of x, y and z: ");
    scanf("%d %d %d", &x, &y, &z);

    if (x < 10)
    if (y > 20)
    if (z < 30)
        printf("A");
    else
        printf("B");
    else
        printf("C");
    else
        printf("D");
        printf("E");
}
